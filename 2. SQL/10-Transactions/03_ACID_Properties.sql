
-- ACID Properties
-- ACID properties ensure reliable processing of database transactions.

-- Example: Explanation of ACID properties
-- Atomicity: All operations in a transaction are completed; if one fails, the whole transaction fails.
-- Consistency: A transaction brings the database from one valid state to another.
-- Isolation: Transactions are isolated from one another until they are completed.
-- Durability: Once a transaction is committed, its changes are permanent, even in the event of a failure.

