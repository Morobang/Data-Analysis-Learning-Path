
-- Normalization (1NF, 2NF, 3NF, BCNF)
-- Normalization is the process of organizing data to reduce redundancy.

-- Example: 
-- 1NF: Ensure that each column contains atomic values and each record is unique.
-- 2NF: Remove partial dependencies; all non-key attributes must depend on the whole primary key.
-- 3NF: Remove transitive dependencies; non-key attributes must depend only on the primary key.
-- BCNF: Every determinant must be a candidate key.

-- NOTE: Actual SQL statements will depend on the specific schema being normalized.
    