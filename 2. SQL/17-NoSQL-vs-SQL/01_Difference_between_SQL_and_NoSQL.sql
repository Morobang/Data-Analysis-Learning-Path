
-- Difference between SQL and NoSQL
-- SQL databases are relational, while NoSQL databases are non-relational.

-- SQL:
-- 1. Structured data with fixed schema.
-- 2. Uses SQL for querying.
-- 3. ACID compliance for transactions.
-- 4. Examples: MySQL, PostgreSQL.

-- NoSQL:
-- 1. Unstructured or semi-structured data.
-- 2. Uses various query languages.
-- 3. BASE model (Basically Available, Soft state, Eventually consistent).
-- 4. Examples: MongoDB, Cassandra.
    