
-- When to use SQL vs NoSQL
-- Choosing between SQL and NoSQL depends on the application requirements.

-- Use SQL when:
-- 1. Data is structured and requires complex queries.
-- 2. ACID transactions are necessary.
-- 3. Relationships between data are important.

-- Use NoSQL when:
-- 1. Data is large-scale and unstructured.
-- 2. Rapid development with changing requirements.
-- 3. High availability and scalability are critical.
    